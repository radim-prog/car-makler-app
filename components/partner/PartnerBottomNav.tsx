export function PartnerBottomNav() {
  const pathname = usePathname();
  const { data: session } = useSession();

  const isVrakoviste = session?.user?.role === "PARTNER_VRAKOVISTE";
  const items = isVrakoviste ? vrakovisteItems : bazarItems;

  return (
    <nav aria-label="Spodní navigace" className="fixed bottom-0 left-0 right-0 z-50 bg-white border-t border-gray-200 pb-[env(safe-area-inset-bottom)]">
      <div className="flex items-center justify-around h-16 max-w-lg mx-auto">
        {items.map((item) => {
          const isActive =
            item.href === "/partner/dashboard"
              ? pathname === "/partner/dashboard"
              : pathname === item.href || pathname.startsWith(item.href + "/");

          if (item.isCenter) {
            return (
              <Link
                key={item.href}
                href={item.href}
                className="flex flex-col items-center justify-center -mt-3"
              >
                <div className="flex items-center justify-center w-14 h-14 rounded-full bg-orange-500 shadow-lg shadow-orange-500/30">
                  {item.icon(false)}
                </div>
                <span className="text-[10px] mt-0.5 text-gray-500">
                  {item.label}
                </span>
              </Link>
            );
          }

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center justify-center min-w-[64px] py-1 ${
                isActive ? "text-orange-600" : "text-gray-400"
              }`}
            >
              {item.icon(isActive)}
              <span className={`text-[10px] mt-0.5 ${isActive ? "font-semibold" : ""}`}>
                {item.label}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
